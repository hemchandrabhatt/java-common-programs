package CommonPrograms;

public class SwitchCaseExample {

	public static void main(String[] args) {
		
		for(int i=1; i<=3; i++){
			switch(3){
			case 1:
				System.out.println("Print 1");
				break;
			case 2:
				System.out.println("Print 2");
				break;
			case 3:
				System.out.println("Print 3");
				break;
				
			default :
				System.out.println("print 0");
			}
		}

	}

}
